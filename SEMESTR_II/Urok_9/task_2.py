# Создаем пустые множества для двух списков чисел
first_set = set()
second_set = set()

# Запрашиваем количество элементов первого списка
size_first = int(input("Введите количество элементов в первом списке: "))

# Наполняем первое множество, выводя номер текущей строки
print(f"--- Ввод элементов первого списка ({size_first} шт.) ---")
for i in range(1, size_first + 1):
    first_set.add(int(input(f"Элемент №{i}: ")))

# Информируем о начале ввода второго списка и правилах завершения
print("\n--- Ввод элементов второго списка ---")
print("Вводите числа по одному. Для окончания ввода напишите слово stop")

# Считываем числа для второго множества, пока не встретим стоп-слово
while True:
    user_input = input("Элемент второго списка (или 'stop' для выхода): ").strip()
    
    # Проверяем, не ввел ли пользователь команду на остановку
    if user_input.lower() == 'stop':
        break
        
    # Игнорируем случайные пустые строки
    if not user_input:
        continue
        
    # Добавляем число во второе множество
    second_set.add(int(user_input))

# Находим количество общих элементов через пересечение множеств
result = len(first_set.intersection(second_set))

# Выводим финальный результат
print(f"\nКоличество совпадающих чисел: {result}")