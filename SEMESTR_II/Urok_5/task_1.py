# Запрашиваем у пользователя целое число
number = int(input("Введите целое число: "))

# Проверяем знак числа (положительное, отрицательное или ноль)
if number == 0:
    print("нулевое число")
else:
    # Определяем знак для формирования строки
    sign_text = "положительное" if number > 0 else "отрицательное"
    
    # Проверяем число на четность и выводим итоговый результат
    if number % 2 == 0:
        print(f"{sign_text} четное число")
    else:
        print("число не является четным")