# Запрашиваем у пользователя длины сторон прямоугольника с поддержкой дробных чисел
side_a = float(input("Введите длину первой стороны: "))
side_b = float(input("Введите длину второй стороны: "))

# Рассчитываем площадь как произведение сторон
area = side_a * side_b

# Рассчитываем периметр как удвоенную сумму сторон
perimeter = 2 * (side_a + side_b)

# Выводим полученные значения площади и периметра
print(f"Площадь прямоугольника: {area}")
print(f"Периметр прямоугольника: {perimeter}")