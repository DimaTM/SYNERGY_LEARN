# Запрашиваем у пользователя входные данные о питомце
pet_type = input("Введите вид питомца: ")
pet_name = input("Введите кличку питомца: ")
pet_age = input("Введите возраст питомца: ")

# Формируем итоговую строку. Используем f-строку для подстановки 
# переменных и экранируем кавычки для корректного отображения клички.
print(f'Это {pet_type} по кличке "{pet_name}". Возраст: {pet_age} года.')