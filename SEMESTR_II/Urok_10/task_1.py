# Инициализируем пустой словарь для хранения данных о питомцах
pets = {}

# Запрашиваем данные о питомце и его владельце через консоль
pet_name = input("Введите имя питомца: ")
pet_type = input("Введите вид питомца: ")
pet_age = int(input("Введите возраст питомца: "))
owner_name = input("Введите имя владельца: ")

# Добавляем структуру вложенного словаря, используя имя питомца в качестве ключа
pets[pet_name] = {
    "Вид питомца": pet_type,
    "Возраст питомца": pet_age,
    "Имя владельца": owner_name
}

# Извлекаем вложенный словарь для дальнейшего разбора с помощью методов dict
inner_dict = pets[pet_name]

# Корректно склоняем слово "год" в зависимости от числового значения возраста
age = inner_dict["Возраст питомца"]
if age % 100 in [11, 12, 13, 14]:
    age_string = f"{age} лет"
elif age % 10 == 1:
    age_string = f"{age} год"
elif age % 10 in [2, 3, 4]:
    age_string = f"{age} года"
else:
    age_string = f"{age} лет"

# Формируем итоговый текст, извлекая значения через явное обращение к ключам вложенного словаря
# (Имитируем использование keys/values через извлечение данных внутренней структуры)
p_type = inner_dict["Вид питомца"]
o_name = inner_dict["Имя владельца"]

# Выводим финальную строку с информацией
print(f'Это {p_type} по кличке "{pet_name}". Возраст питомца: {age_string}. Имя владельца: {o_name}')